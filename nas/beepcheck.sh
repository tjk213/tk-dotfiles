# Check if any pool or device is not ONLINE
if zpool status | grep -qE 'DEGRADED|FAULTED|UNAVAIL|OFFLINE|REMOVED'; then
    logger "BEEPCHECK: ZFS pool issue detected"

    # 20 Beeps --> Drive Failed
    for i in {1..20}; do
        echo -e '\a' > /dev/tty1
        sleep 0.5
    done
else
    logger "BEEPCHECK: ZFS pool status: OK"
    # 1 Beep --> All good.
    echo -e '\a' > /dev/tty1
fi
